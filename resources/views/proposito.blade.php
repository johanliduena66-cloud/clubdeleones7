								<p>
									Como Leones, nos atenemos a normas  de ética elevadas en todo lo que hacemos. Nuestros socios y clubes se dedican a  servir a los demás, y se nota. Abogamos por la bondad en nuestras comunidades y  trabajamos activamente para mejorar las vidas de todas las personas.<br>
									<strong>&nbsp;Nuestro propósito</strong> <br>
									<strong>Organizar</strong>,  constituir y supervisar clubes de servicio que se conocerán como clubes de  Leones.<br>
									<strong>Coordinar</strong>&nbsp;las  actividades y estandarizar la administración de los clubes de Leones.<br>
									<strong>Crear</strong>&nbsp;y  fomentar un espíritu de entendimiento entre los pueblos del mundo.<br>
									<strong>Fomentar&nbsp;</strong>los  principios del buen gobierno y de la buena ciudadanía.<br>
									<strong>Participar&nbsp;</strong>activamente  en el bienestar cívico, cultural, social y moral de la comunidad.<br>
									<strong>Unir</strong>&nbsp;a  los clubes en vínculos de amistad, buen compañerismo y entendimiento mutuo.<br>
									<strong>Proporcionar</strong>&nbsp;a  los socios de los clubes un medio de discusión para el amplio y libre estudio  de todos los asuntos de interés público, con la excepción de asuntos de  política partidista y sectarismo religioso.<br>
									<strong>Alentar</strong>&nbsp;a  personas con vocación de servicio a servir en la comunidad sin buscar  recompensa financiera personal, y alentar la eficiencia y fomentar la práctica  de elevadas normas de ética en el comercio, la industria, las profesiones,  obras públicas y proyectos privados.</p>
                                <p><strong>Nuestro código de ética</strong> <br>
                                  <strong>Mostrar&nbsp;</strong>mi  fe en la bondad de mi vocación aplicándome diligentemente hasta lograr una buena  reputación por la alta calidad de mi servicio.<br>
                                  <strong>Buscar</strong>&nbsp;el  éxito y exigir solo la remuneración o ganancia justa que pueda merecer, pero  rehusar la ganancia o recompensa que pudiera resultar en menoscabo o pérdida de  mi dignidad, como efecto del aprovechamiento de alguna ventaja injusta o de  acciones dudosas mías.<br>
                                  <strong>Recordar</strong>&nbsp;que  para desarrollar mi negocio no es necesario destruir el de otro; ser leal con  mis clientes y sincero para conmigo mismo.<br>
                                  <strong>Resolver</strong>&nbsp;cualquier  duda que surja sobre el derecho o ética de mi posición o acción hacia los demás  aún a costa de mí mismo.<br>
                                  <strong>Practicar</strong>&nbsp;la  amistad como un fin y no como un medio. Sostener que la amistad verdadera no  existe por razón del servicio que se nos ha prestado, sino que la amistad  verdadera no exige nada a cambio, pero acepta el servicio con el mismo espíritu  que se ha dado.<br>
                                  <strong>Tener</strong>&nbsp;siempre  presente mis obligaciones de ciudadano para con mi nación, mi estado y mi  comunidad, profesándoles mi lealtad inquebrantable de pensamiento, palabra y  obra. Dedicarles generosamente mi tiempo, mi trabajo y mis recursos.<br>
                                  <strong>Ayudar</strong>&nbsp;al  prójimo, consolar al atribulado, fortalecer al débil y socorrer al menesteroso.<br>
                                  <strong>Ser mesurado</strong>&nbsp;en  la crítica y liberal en el elogio; construir y no destruir.</p>
