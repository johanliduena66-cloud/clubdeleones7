							<h2 class="colorlib-heading">HISTORIA  DEL SERVICIO EN EL CLUB DE LEONES DE COROZAL</h2>
								<p>
								  El  Club de Leones de corozal fue fundado por 24 filántropos de esta ciudad, el 04  de noviembre de 1954.<br>
								  Es  una organización privada sin ánimo de lucro (ESAL).<br>
								  Actualmente  cuenta con 37 socios, 19 mujeres y 18 hombres voluntarios al servicio de la  población vulnerable de corozal Leones, damas leones y domadoras, también  cuenta con un Club Leo denominado Corozal Nuevo Horizonte para formar líderes y  buenos ciudadanos con una membresía de 18 socios, 13 mujeres y 5 hombres <br>
                                  <strong>NUESTRAS CAUSAS  GLOBALES</strong><br>
								  Cada&nbsp;trayectoria comienza con un primer paso. A  menudo, para llevar la esperanza a donde más se necesita solo hace  falta&nbsp;ofrecer servicios, dar una palabra de ánimo, hacer un acto de  generosidad.<br>
								  Durante los últimos 100 años, los actos&nbsp;caritativos  de los Leones y Leos se han multiplicado y han cruzado fronteras, océanos y  continentes. Con más de 1,4 millón de socios, ahora tenemos la oportunidad de  cambiar de verdad el mundo.<br>
								  Es por ello que estamos unificando nuestro servicio global  en torno a cinco áreas de necesidad. Estas causas globales presentan un desafío  considerable para la humanidad, y creemos que nos toca a nosotros el hacerles  frente.<br>
                                  <strong>Diabetes</strong><br>
								  Nosotros  servimos para reducir la prevalencia de la diabetes y mejorar la calidad de  vida de las personas diagnosticadas con esta enfermedad.<br>
  								  <strong>Visión</strong><br>
								  Nosotros  servimos para prevenir la ceguera evitable y mejorar la calidad de vida de las  personas ciegas y con discapacidad visual.<br>
  								  <strong>Hambre</strong><br>
								  Nosotros  servimos para asegurar que todos los miembros de la comunidad tengan acceso a  alimentos nutritivos.<br>
  								  <strong>Medio ambiente</strong><br>
								  Nosotros  servimos para proteger y restaurar de manera sostenible el medio ambiente y  mejorar así el bienestar de todas las comunidades.<br>
 								 <strong>Cáncer infantil</strong><br>
								  Los  Leones y Leos apoyan las necesidades de los niños y familias afectados por el  cáncer infantil a través de actividades de servicio impactantes.</p>
                                <p>Desde  hace 67 años, el club de leones de corozal presta servicios de salud abajo  costo a la población vulnerable. <br>
                                  Inicialmente  tuvimos una clínica de hidratación a niños que tenían problemas de  gastroenteritis. Posteriormente y debido a los cambios de la medicina esta sala  de hidratación fue cerrada y se creó la IPS CLUB DE LEONES DE COROZAL, la cual  presto servicios de medicina general ambulatoria de baja complejidad y así  fuimos anexando otros servicios de medicina especialidad como fueron  cardiología, ginecología, gastroenterología oftalmología, alergólogo,  otorrinolaringólogo. <br>
                                  Posteriormente  y debido a los múltiples requisitos impuestos por el ministerio de salud, a  través del Departamento Administrativo De Seguridad Social en Salud de Sucre <strong>DASSSALUD, </strong>nos vimos obligados a  prestar estos servicios en forma de brigada.<br>
                                  Actualmente  estamos en el proceso de inscripción y reactivación de la IPS en medicina  general ambulatoria de baja complejidad, para lo cual estamos realizando  actividades tales como autoevaluación, mantenimiento y certificación del  sistema eléctrico y de la planta física, dotación de equipos médicos,  organización uy optimización del sistema contable y tributario, certificación  de suficiencia patrimonial y financiera, certificación de la seguridad de la  edificación plan hospitalario para emergencias, cronograma de mantenimientos y  hoja de vida de los equipos médicos, entre otras actividades.<br>
                                  También  prestamos servicios a población vulnerables en las causas globales cáncer  infantil: donación de tapas plásticas Convenio Con La Fundación Sanar, mitigación  del hambre se han realizado ollas comunitarias en sectores vulnerables,  mercados a moto taxis , diabetes realización de 3 brigadas las cuales se hace  cada 15 días, visión brigadas con diferentes fundaciones Unidad  Visual San Clemente, Tigre Vsión y Mundo  Vsión y también tamizaje para prevención de  la ambliopía en niños de 6 meses a 6 años lo mismo quemedio ambiente,  brigadas de limpieza - reciclaje y huertas  caseras</p>

