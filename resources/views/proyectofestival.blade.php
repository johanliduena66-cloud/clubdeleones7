								<h2 class="colorlib-heading">ANTECEDENTES Y TRAYECTORIA DEL PROYECTO</h2>
								<p>El  club de Leones de Corozal tiene aproximadamente 20 años de venir ejecutando  proyectos culturales y artísticos tales como Cachorra del año, Chica Simpatía y  hace 15 años se inició el proyecto revelación Juvenil que posteriormente se  denominó REINADO ESTUDIANTIL DEL PORRO, y a partir de 2016 toma el nombre de  FESTIVAL ESTUDIANTIL DEL PORRO. </p>
                                <p>Durante  el año 2020 el Club de Leones realiza la XV versión del FESTIVAL ESTUDIANTIL  DEL PORRO modo virtual debido a la pandemia provocada por el COVID-19 que nos  obligó a replantearnos algunas metas y la forma presencial fue reemplazada por  el modo virtual.</p>
                                <p>El  Festival Estudiantil del Porro desde el punto de vista cuantitativo ha  estimulado el aprecio por nuestras tradiciones culturales. Para el año 2020 se  cambió la estrategia debido como dijimos anteriormente a la pandemia provocada  por el COVID-19, por ello elaboramos una cartilla denominada HISTORIA DEL PORRO  y se articuló con el currículo de las escuelas oficiales desde tercero  elemental hasta el grado 11º.</p>
                                <p>En  ella presentamos la historia del porro en Colombia, qué es el porro,  instrumentos con los cuales se toca, vestimenta y en especial como se baila el  Porro. Esto nos da la oportunidad de llegar a más de 6.000  estudiantes quienes a través de la clase de  artística y educación física, conocen este aire musical y aprenden a bailarlo.  Así mismo el concurso de parejas bailadoras en las modalidades infantil,  juvenil y aficionados se transmitieron por las redes sociales, lo que permitió  que muchas personas del municipio, del Departamento, de la región y del país  puedan apreciar este hermoso festival. También se transmitieron por las redes  sociales virtualmente utilizando los servicios virtuales de LIVE STREAMING lo  que hizo que muchas personas pudieran acceder a la producción de nuestro  Festival. </p>
                                <p>También  se creó un espacio de muestra virtual que sirvió de vitrina para mostrar los  talentos del Municipio, exposiciones de pintura donde se dio a conocer pintores  como Plinio Lambraño, Rosa Martínez, Luis Castro, Eudilio Muleth, Nidia Gómez  Cáceres y Pilar Piedrahita; artesanías de la ciudad de Morroa. Se pudo apreciar  la gastronomía Corozalera mostrando la forma en que se produce el diabolín a  cargo de Rogelio Márquez; la bolita de leche a cargo de Luis Lara; el mote de queso,  la ensalada de berenjena, el jugo de corozo elaborado por la señora Rosario  Hoyos de Muleth y los famosos pasteles producidos por la señora Olga Piña. Así  mismo las fiestas tradicionales especialmente la gigantona personaje central  del carnaval de Corozal.</p>
                                <p>Por  ultimo mostramos la institución educativa Liceo Carmelo Percy Vergara como un  polo de desarrollo donde se dan innovaciones para hacer frente a la pandemia  del COVID-19 con la estrategia Liceo en casa, lo mismo que se mostraron las  investigaciones de ondas Colciencias en curso en esta institución como son  investigación del mito La Diabla Paria liderados por Pedro Cabarcas y Yudis  Gutiérrez, investigación Hacia Una Sana Convivencia liderado por Santamaría  Correa y Marlene Olivera, el proyecto Educación Ambiental que adquiere gran  importancia en estos momentos por los cambios que lidera a nivel institucional  en las relaciones con el medio ambiente, con los demás y consigo mismo,  liderado por Denys Pérez y Luis Mendoza; así mismo el proyecto de investigación  sobre la inclusión en el Liceo Carmelo Percy Vergara liderado por el  departamento de Bienestar Institucional, coordinado por la Orientadora Escolar  María Elena Guerrero. Por otra parte se compartieron experiencias de semilleros  de investigación de ondas y Redcolci como fueron las actitudes ambientales de  la<br>
                                  comunidad  estudiantil de la I.E. Liceo Carmelo Percy Vergara y los indicadores por  tolerancia al estrés hídrico en hortalizas de berenjena y habichuela liderado  por el Mg. Carlos Vergara; por último se pudo apreciar el trabajo realizado por  la docente Martha Mogollón ¿Cómo facilitar la información y comunicación entre  los estudiantes de la comunidad educativa liceísta para construir comunidad  comunicadora y cultural?.</p>
                                <p>Por  otra parte se realizó un concurso de afiche promocional para lo cual se  publicaron parámetros del concurso, se seleccionaron 3 jurados: Rosa Martínez,  Miguel Albadán y Oscar Padilla; los cuales seleccionaron un afiche ganador.<br>
                                  De  gran importancia fueron los talleres realizados por los gestores culturales y  docentes folcloristas en las instituciones educativas de los cuales se  realizaron 7 videoclips y 10 Jingles publicitarios del Festival. En estos  momentos el proyecto Festival Estudiantil del Porro se ha convertido en un  evento regional y trasciende las fronteras del departamento de Sucre, llegando  por las redes sociales no solo a los departamentos de la costa sino también al  interior del país. Todas las actividades se publican por las redes sociales y  por la prensa hablada y escrita, resaltando que es un evento apoyado por el  Ministerio de Cultura-Programa Nacional de Concertación Cultural. Para el año  2020 se homenajeo a la profesora Flor María González quien además de gestora  cultural es conocida como compositora, ganadora en concursos como: Festival  Nacional de Gaitas Francisco Llirene modalidad Canción inédita (1991), cabe  señalar que fue la primera mujer en participar y ganar en canción inédita con  la canción Luna Cumbiambera la cual fue utilizada para la banda sonora de la película  siniestro de Ovejas de Ernesto McCausland; es compositora de varias canciones  destacándose la cumbia Tamborero que es muy sonada en los carnavales del  atlántico; desde 2018 trabaja con niños y jóvenes  en la formación de música de Gaita, siendo  producto de este trabajo 2 grupos de Gaita Infantil y Juvenil denominados  Herencia Carangano; de todo este trabajo de la homenajeada se realizó un video.</p>
                                <p>Desde  el punto de vista cualitativo brindamos espacios virtuales lúdicos a los niños,  niñas, jóvenes y adultos participantes, fortaleciendo su autoestima y  autoconcepto y desarrollando su inteligencia emocional, de tal manera que  podemos mencionar como logros que nuestros participantes lideran escuelas de  formación dancísticas y cultural en el Caribe Colombiano y además reconocidos  como gestores culturales como es el caso<br>
                                  de  la Escuela Nubalé cuyo director es Jaiber Hernández, es reconocida en concursos  interregionales como El Caribe Danza y el Festival de la Cumbia en el Banco  Magdalena. De igual manera permitimos a las personas aficionadas y de la  tercera edad interactuar en nuestro Festival y como dijimos anteriormente  creamos una vitrina para mostrar los talentos Corozaleros donde se muestran los  resultados, avances y creatividad de los talentos de nuestro municipio con el  propósito que sean conocidos en la región y en el país. </p>
                                <p>El  Club de Leones de Corozal durante 15 años consecutivos y 14 ediciones ha  realizado este proyecto cultural.<br>
                                  En  las fases anteriores el Festival ha logrado construir su identidad identificándose  en la región Caribeña y en el país como una manifestación Cultural propia  articulada con las festividades de la región especialmente el carnaval de  Corozal.</p>
                                <p>Desde  el punto de vista cuantitativo hemos logrado que nuestros niños, niñas y jóvenes  en una cuantía de 600<br>
                                  desarrollen  el aprecio por nuestra tradiciones y participen en los desfiles, talleres que  se imparten en el Club y en las escuelas de básica primarias, secundaria y  media académica del casco urbano y rural del municipio de Corozal. También el  año anterior 2019 se realizó un foro presencial y uno virtual que respondía la  pregunta ¿Cómo vincular el Festival Estudiantil del Porro a los carnavales de  Corozal?.</p>
                                <p>Es  importante anotar que a la fecha hemos homenajeado a 12 estudiosos (En el año  2020 Flor María González) y 2 difusores de nuestra manifestación culturales El  Porro (El Meridiano de Sucre y El Festival del Porro en San Pelayo), se ha  editado y se tiene en internet en la página web del Festival una cartilla  didáctica de 20 paginas ?CARTILLA COMO SE BAILA EL PORRO? y también se diseñó  una revista donde se evidencian las actividades realizadas en el Festival, se  diseñó un blog y se creó un espacio radial denominado HABLEMOS DE PORRO, en el  cual intervinieron gestores culturales, docentes y folcloristas que explicaban  a la ciudadanía los conocimientos ancestrales relacionados con el porro.  También se han editado cuatro radioclips, 15 videos, un grupo de whatsapp.</p>
                                <p>El  proyecto cultural FESTIVAL ESTUDIANTIL DEL PORRO tiene como fin primordial  brindar espacios temporales, físicos y virtuales a los (as) jóvenes de Corozal,  de la región caribeña y del país para que identifiquen, se apropien e  interioricen manifestaciones de nuestro folclor, como es el porro, aire musical  y dancístico, representativo de la región sabanera y caribe. Así mismo se  pretende contribuir en la construcción de la<br>
                                  identidad cultural, cultivando sus aptitudes artísticas y  de liderazgo, y con ello fomentar el desarrollo de una cultura de paz a través  del apoyo de actividades culturales que desarrollen la inteligencia emocional y  al mismo tiempo se prevengan riesgos psicosociales en la población infantil,  juvenil y adultos mayores, desplazada y vulnerable.<br>
                                  El  proyecto se desarrolla en tres (03) etapas:</p>
                                <p>1.  ETAPA DE PRE-EVENTO. En esta etapa se hacen visitas a las escuelas, se  contratan instructores para capacitar a niños(as) y jóvenes en el manejo  teórico práctico de la danza del porro; en el conocimiento del vestuario  apropiado para esta danza; se realiza el concurso de afiche promocional, los  foros o conversatorio sobre aspectos relacionados con el nacimiento, desarrollo  y futuro del porro. Igualmente se realizan las diferentes estrategias promocionales  del evento a través de la prensa hablada y escrita, la televisión, la página  web y las redes sociales; lo mismo que gestiones para la consecución de  recursos y apoyo a este magno evento.</p>
                                <p>Desde  el punto de vista administrativo, el representante legal de la institución  designa un coordinador del evento, quien gestionara, organizara y liderara las  actividades del proyecto.<br>
                                  Este  a su vez organiza con los socios del club varios comités que trabajaran en las  diferentes actividades propias del evento.<br>
                                  Durante  el año 2020 debido a la pandemia provocada por el COVID-19 hubo la necesidad de  recurrir al modo virtual, por eso esta etapa se realizó a través de las  diferentes redes sociales como Facebook, whatsapp, Instagram; plataformas como  zoom; llamadas telefónicas, etc.</p>
                                <p>2.  ETAPA DE EVENTO. Se inicia con la inauguración, lanzamiento del festival y  reconocimiento de una entidad, estudioso, difusor, compositor o investigador  del porro con la entrega del premio la Trompeta Dorada. De igual manera se  realiza el tradicional desfile folclórico de comparsas y parejas bailadoras,  foros y exposiciones de las costumbres y tradiciones del Municipio, el  porrodromo y la ronda final de Parejas Bailadoras. Actividades éstas que le  permite a toda la ciudadanía observar el baile, los trajes típicos y los  accesorios propios de la danza del porro.<br>
                                  De  igual manera y debido a la modalidad virtual todas las actividades y concursos  de comparsas y parejas  bailadoras se  realizaron virtualmente y como dijimos anteriormente en la ventana virtual se  mostraron los talentos del municipio, nuestras costumbres y tradiciones; con  relación al baile del porro, los trajes típicos y accesorios de esta danza se  mostraron a todos los estudiantes del municipio de las escuelas oficiales de  tercero<br>
                                  de  primaria hasta el grado once, a través de una cartilla virtual denominada  ?HISTORIA DEL PORRO? que en su segunda edición se amplió y se articuló con las  áreas de artística y educación física.<br>
                                  Así mismo se hizo con el concurso de afiche  promocional.</p>
                                <p>3.  ETAPA POST-EVENTO. En esta etapa se rinde el informe final de resultados al  Ministerio de Cultura, previa evaluación del evento, se diligencia el acta de  liquidación del convenio que nos permitió desarrollar el proyecto. Así mismo se  alimenta la página web a fin de conservar las memorias de la actividad. Se hace  entrega formal de los premios y se cancelan los contratos a los músicos,  artistas, logística y equipo técnico que nos permitieron realizar y  participaron en el Festival.<br>
                                  Finalizado el evento se realiza la evaluación interna por  parte de los socios en una asamblea extraordinaria y externa por parte de  personas de reconocida idoneidad cultural del Municipio, las escuelas y  colegios participantes a fin de enriquecer la justificación del proyecto. Se  evalúan los aspectos financieros, el impacto social, el desarrollo de las  etapas y posibilidades de mejora, para ello se aplica un instrumento de  evaluación con la técnica matriz DOFA</p>
