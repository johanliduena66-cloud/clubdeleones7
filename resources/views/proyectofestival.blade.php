								<h2 class="colorlib-heading">Objetivos</h2>
								<p>Impulsar  y defender la danza del Porro para que nuestros niños (as), jóvenes y adultos  mayores lo bailen, desarrollen su inteligencia emocional y quinestésica,  olviden la violencia y construyan lazos de amistad y fraternidad, fortaleciendo  la identidad sabanera con la interiorización de conocimientos relacionados con  el porro, sus características, clasificación, ejecución y conocimiento de  nuestra diversidad artística y cultural. </p>
                            <p>&nbsp;</p>
                            <p><strong>OBJETIVOS ESPECIFICOS</strong></p>
                            <ul>
                              <li> Estimular los talentos artísticos, dancísticos y  musicales del municipio, de la región y del país a través del concurso de  parejas bailadoras de porro en categorías infantil, juvenil, aficionada,  profesional y comparsas, diseño y pintura del afiche promocional y exaltación,  reconocimiento y premiación de los mejores y a un estudioso o difusor de la  música del porro.</li>
                              <li> Realizar talleres teórico- práctico ce cómo se  baila el porro en las escuelas urbanas y rurales de básica primaria del  Municipio, con niños y niñas con edades entre 8 y 13 años para que adquieran  conocimientos y destrezas en la ejecución del baile del porro y de sus  instrumentos musicales y permitiendo que valoren el desarrollo y tendencias  actuales de la música raizal del caribe colombiano.</li>
                              <li> Difundir las diferentes actividades del festival  estudiantil del porro , a través de las redes sociales, blog, pagina web,  radio, televisión, prensa escrita, pancartas, pasacalles, afiches, pendones,  vayas, murales y estiquers dando crédito al Ministerio de Cultura.</li>
